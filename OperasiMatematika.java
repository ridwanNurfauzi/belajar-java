public class OperasiMatematika {
    public static void main(String[] args) {
        int angka = 7;

        angka = angka + 2;
        System.out.println(angka);
        
        angka = angka - 2;
        System.out.println(angka);
        
        angka = angka * 2;
        System.out.println(angka);
        
        angka = angka / 2;
        System.out.println(angka);
        
        angka = angka % 2;
        System.out.println(angka);
        
        
        angka++;
        System.out.println(angka);
        
        angka--;
        System.out.println(angka);
    }
}
